import { prisma } from "@/lib/prisma";


export async function AllUsers() {
 

  // Fetch all users with their posts
  const allUsers = await prisma.user.findMany({
    include: {
      posts: true,
    },
  });
  console.log("All users:", JSON.stringify(allUsers, null, 2));
  return allUsers
}

